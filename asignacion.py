numero = 10

print("Numero original: ", numero)

# Quiero incrementar mi numero en 2

numero = numero + 2

print("Numero incrementado: ", numero)

# Otra forma de incrementar mi numero en 2

numero += 2

print("Numero incrementado 2°: ", numero)

# Quiero decrementar mi numero en 4

numero = numero - 4

print("Numero decrementado: ", numero)

# Otra forma de decrementar mi numero en 2

numero -= 4

print("Numero decrementado 2°: ", numero)
